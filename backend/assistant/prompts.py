def _render_tree(nodes: list[dict[str, Any]], depth: int = 0) -> list[str]:
    """Дерево категорій → рядки «  - Холодильники [id=5609710, slug=kholodylnyky] (34)».

    Порядок задає сервіс (`order_by("sort_order", "id")`), тому рендер відтворюваний байт-у-байт.
    Порожні категорії до дерева не потрапляють взагалі (tree.py) — і це правильно: пропонувати
    людині категорію, яка веде на порожню сторінку, гірше, ніж не пропонувати нічого.
    """
    lines: list[str] = []
    indent = "  " * depth
    for node in nodes:
        lines.append(
            f"{indent}- {node['name']} "
            f"[id={node['external_id']}, slug={node['slug']}] ({node['products_count']})"
        )
        lines.extend(_render_tree(node["children"], depth + 1))
    return lines


def _render_facets(meta: dict[str, dict[str, Any]]) -> list[str]:
    """Словник фасетів: код → підпис + приклади значень.

    ⚠️ Сортування ЯВНЕ (`sorted`), а не «як лягло в dict». Порядок ключів у dict тут стабільний
       за побудовою, але покладатися на це — саме той клас недетермінізму, що тихо ламає кеш.
    """
    lines: list[str] = []

    ordered = sorted(
        meta.items(),
        key=lambda item: (
            item[1].get("group_sort", 0),
            item[1].get("sort_order", 0),
            item[0],
        ),
    )

    for code, info in ordered:
        labels: dict[str, str] = info.get("labels", {})
        order: list[str] = list(info.get("order", []))
        # Спершу явний порядок з адмінки, потім усе інше — за алфавітом (детерміновано).
        values = order + sorted(v for v in labels if v not in order)
        shown = values[:_FACET_VALUES_IN_PROMPT]

        sample = ", ".join(f"{value}={labels.get(value, value)}" for value in shown)
        if len(values) > len(shown):
            sample += f", … (+{len(values) - len(shown)})"

        unit = f" [{info['unit']}]" if info.get("unit") else ""
        lines.append(f"- {code} — {info['label']}{unit}: {sample or '—'}")

    return lines


def build_catalog_map(lang: str) -> str:
    """Другий system-блок: карта категорій + словник фасетів.

    ⚠️ Ходить у БД/Redis (дерево і фасети кешовані). Викликати з async-коду тільки через
       `sync_to_async(..., thread_sensitive=True)`.
    """
    tree_lines = _render_tree(tree.get_category_tree(lang))
    # ⚡ ЕКОНОМІЯ ПРЕФІКСА. У промпт кладемо лише УНІВЕРСАЛЬНІ фільтри (group="" — бренд,
    # наявність, стан, країна, оплата частинами): вони є в КОЖНІЙ категорії і потрібні
    # найчастіше (в наявності / за брендом / за ціною), тож тримати їх під рукою дешевше,
    # ніж щоразу ходити в get_facets. А категорійні атрибути (обʼєм, діагональ, no-frost —
    # їх ~24, і вони специфічні) з промпту прибрані: вони дублювали інструмент get_facets,
    # роздували префікс на ~1.7к токенів у КОЖНОМУ запиті й до того ж давали лише приклади
    # значень, а не живі лічильники. Тепер модель бере їх з get_facets — там вони точні.
    system_meta = {
        code: info for code, info in get_facet_meta(lang).items() if not info.get("group")
    }
    facet_lines = _render_facets(system_meta)

    return "\n".join(
        [
            "═══ КАТЕГОРІЇ КАТАЛОГУ (id / slug — для інструментів; у дужках — к-сть товарів) ═══",
            "",
            *tree_lines,
            "",
            "Категорій, яких тут немає, у магазині не існує. Не вигадуй їх і не обіцяй товар",
            "з категорії, якої немає в цьому списку.",
            "",
            "═══ УНІВЕРСАЛЬНІ ФІЛЬТРИ (є в кожній категорії; код — підпис: значення) ═══",
            "",
            *facet_lines,
            "",
            "Це лише спільні осі. Фільтри, специфічні для категорії (обʼєм, діагональ, кількість",
            "камер тощо), та точні значення з живими лічильниками дає get_facets — викликай його",
            "для конкретної категорії й саме звідти бери slug'и для search_products і",
            "build_catalog_link. Не вгадуй коди фільтрів, яких немає ні тут, ні в get_facets.",
            "",
        ]
    )


def build_system(locale: str, *, cacheable: bool = True) -> list[dict[str, Any]]:
    """System-блоки для messages.create/stream.

    `cacheable` — результат `should_cache()`, поміряний ОДИН раз (на старті процесу, не на
    кожен запит: count_tokens — це мережевий виклик). Якщо False, `cache_control` не ставимо:
    він однаково не спрацював би, а так у коді видно, що це усвідомлене рішення, а не забутий
    breakpoint.

    ⚠️ Мова відповіді — АРГУМЕНТ, а не глобальний стан (див. catalog/services/lang.py).
    """
    lang = normalize_lang(locale)

    rules = RULES + (
        f"\nМова відповіді за замовчуванням — {_LANG_NAME.get(lang, _LANG_NAME['uk'])} "
        f"(користувач зараз на {lang}-версії сайту). Якщо він пише іншою — відповідай його мовою.\n"
    )

    blocks: list[dict[str, Any]] = [{"type": "text", "text": rules}]

    tail: dict[str, Any] = {"type": "text", "text": build_catalog_map(lang)}
    if cacheable:
        # ttl=1h, а не 5m: карта каталогу однакова для ВСІХ сесій усіх користувачів, тож
        # переписувати її щоп'ять хвилин — це платити 1.25× за запис знову і знову. Година
        # переживає і нічну тишу, і ранковий сплеск.
        tail["cache_control"] = {"type": "ephemeral", "ttl": "1h"}
    blocks.append(tail)

    return blocks


