def min_cache_prefix(model: str) -> int:
    """Мінімальний кешований префікс моделі, у ТОКЕНАХ.

    Джерело — `translation.conf.MODELS` (там це вже звірено з докою). Невідома модель →
    консервативні 4096: краще не поставити breakpoint, ніж поставити мертвий.
    """
    spec = translation_conf.MODELS.get(model)
    return spec.min_cache_prefix if spec is not None else DEFAULT_MIN_CACHE_PREFIX


def count_prefix_tokens(
    client: Any,
    system: list[dict[str, Any]],
    tools: list[dict[str, Any]],
    model: str,
) -> int:
    """Скільки токенів у префіксі (tools + system) за токенізатором САМОЇ моделі.

    ⚠️ `tools` рендеряться ПЕРЕД `system`, тому схеми інструментів теж входять у кешований
       префікс — і рахувати їх треба разом із system, інакше ми недооцінимо префікс і не
       поставимо breakpoint там, де він спрацював би.

    `messages` — обов'язкове поле API, тому передаємо мінімальну заглушку; її внесок у лічильник
    одиниці токенів і на рішення «≥ 4096» не впливає.
    """
    response = client.messages.count_tokens(
        model=model,
        system=system,
        tools=tools,
        messages=[{"role": "user", "content": "."}],
    )
    return int(response.input_tokens)


def should_cache(
    system: list[dict[str, Any]],
    tools: list[dict[str, Any]],
    model: str,
    *,
    client: Any = None,
) -> bool:
    """Чи перекриває префікс мінімум моделі — тобто чи має сенс `cache_control`.

    🔴 Міряємо `count_tokens()`, а НЕ `len(str)`. Це токени, а не символи: на кирилиці різниця
       в 2–3 рази, і «на око» рішення про 4096 приймати не можна.

    Немає клієнта / немає ключа / API не відповів → False + warning. Логіка: кеш — це
    оптимізація, і вона не сміє валити чат. Але й тихо вдавати, що кеш працює, теж не сміє —
    тому warning у лог, а не мовчазний True.
    """
    if client is None:
        log.warning("assistant: гейт кешу без клієнта — cache_control не ставимо")
        return False

    try:
        tokens = count_prefix_tokens(client, system, tools, model)
    except Exception:  # межа підсистеми: кеш не сміє валити чат
        log.exception("assistant: count_tokens впав — cache_control не ставимо")
        return False

    threshold = min_cache_prefix(model)
    if tokens < threshold:
        log.warning(
            "assistant: префікс %d ток. < мінімуму %d для %s — cache_control не ставимо "
            "(він би мовчки не спрацював)",
            tokens,
            threshold,
            model,
        )
        return False

    log.info("assistant: префікс %d ток. ≥ %d — кешуємо system (ttl=1h)", tokens, threshold)
    return True
