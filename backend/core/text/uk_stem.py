"""Український стемер (ADR-009, шар 2).

ЧОМУ ВІН ВЗАГАЛІ ІСНУЄ
----------------------
У PostgreSQL **немає** вбудованої конфігурації для української: `to_tsvector('ukrainian', …)`
кидає помилку, а наша TS-конфігурація `uk` (створена міграцією core/0001_extensions) —
це `COPY = simple` + `unaccent`, тобто вона нормалізує регістр і діакритику, але **не стемить
узагалі**. Наслідок без цього модуля: запит «холодильники» не знаходить «Холодильник Gorenje»,
бо в індексі лежить лексема `холодильник`, а в запиті — `холодильники`. Тобто основний сценарій
пошуку в магазині побутової техніки просто не працює.

Hunspell-словник `uk_UA` дав би кращу якість, але вимагає ВЛАСНОГО образу Postgres (словники
живуть у `$SHAREDIR/tsearch_data`) — це власний білд БД, складніший restore і кінець можливості
переїхати на managed-Postgres. ADR-009 свідомо тримає hunspell як escape hatch на Етап 4.

ГОЛОВНЕ ПРАВИЛО: СИМЕТРІЯ
-------------------------
Стемер застосовується РІВНО ДВІЧІ і РІВНО ОДНАКОВО:
  1. при побудові `search_vector_uk`  (catalog/services/search_index.py) — текст стемиться в
     Python ПЕРЕД `to_tsvector('uk', …)`;
  2. при парсингу пошукового запиту   (catalog/services/search.py).

Саме симетрія, а не «лінгвістична правильність», робить пошук робочим: якщо і документ, і запит
згорнулись в ту саму форму — вони збігаються. Тому надлишкове відсікання («холодильник» →
«холодильн») не є багом, доки воно застосовується з обох боків. А ось АСИМЕТРІЯ (застемити індекс
і забути застемити запит) ламає пошук повністю й мовчки.

⚠️ Латиниця НЕ стемиться. Бренди й моделі («Gorenje», «NRK6202», «No Frost») мусять лишатись
   як є: відсікання «-е» з «Gorenje» зробило б «Gorenj» і вбило б пошук по бренду.
"""

from __future__ import annotations

import re

__all__ = ["stem_text", "stem_word"]

# Мінімальна довжина основи. Нижче цієї межі суфікс НЕ відсікаємо — інакше короткі слова
# («піч», «ніж», «газ») стираються в порожнечу і зливаються між собою.
MIN_STEM = 4

# Слова, які стемити НЕ МОЖНА (омонімія або технічний термін, який ми не хочемо руйнувати).
# Ключ — слово в нижньому регістрі, значення — форма, яку кладемо і в індекс, і в запит.
EXCEPTIONS: dict[str, str] = {
    "піч": "піч",
    "печі": "піч",
    "пічі": "піч",
    "газ": "газ",
    "газу": "газ",
    "лід": "лід",
    "льоду": "лід",
    "ніж": "ніж",
    "ножі": "ніж",
    "ножів": "ніж",
    "вага": "ваг",
    "ваги": "ваг",
    "миша": "миш",
    "миші": "миш",
}

# Суфікси — від НАЙДОВШОГО до найкоротшого (перший збіг виграє).
# Порядок критичний: якби «и» стояло перед «ами», то «холодильниками» відсіклося б у
# «холодильникам», а не в «холодильник».
_SUFFIXES: tuple[str, ...] = tuple(
    sorted(
        (
            # прикметники (родовий/давальний/орудний/місцевий)
            "ського",
            "цького",
            "ського",
            "ими",
            "ого",
            "ому",
            "ої",
            "ою",
            "ій",
            "ий",
            "их",
            "ім",
            "им",
            "ої",
            "ій",
            # іменники: множина
            "ами",
            "ями",
            "ах",
            "ях",
            "ів",
            "ам",
            "ям",
            "ов",
            # іменники: однина (непрямі відмінки)
            "ові",
            "еві",
            "ем",
            "ом",
            "ою",
            "ею",
            "ці",
            "зі",
            "сі",
            # дієслівні / інші
            "ти",
            "ся",
            "ність",
            "ість",
            # найкоротші — ОСТАННІ
            "а",
            "е",
            "и",
            "і",
            "о",
            "у",
            "ю",
            "я",
            "ї",
            "й",
        ),
        key=len,
        reverse=True,
    )
)

# Слово вважаємо «українським» (тобто стемимо), лише якщо в ньому є кирилиця.
_CYRILLIC = re.compile(r"[а-яїієґё]", re.IGNORECASE)
# Токенізація: літери/цифри (будь-якої абетки), плюс дефіс і крапка всередині моделей (NRK-6202).
_TOKEN = re.compile(r"[^\W_]+", re.UNICODE)


def stem_word(word: str) -> str:
    """Згортає одне слово до основи.

    Латиниця, цифри й моделі повертаються БЕЗ ЗМІН (див. шапку модуля).
    """
    w = word.lower()

    if w in EXCEPTIONS:
        return EXCEPTIONS[w]

    # Нема кирилиці → це бренд/модель/число. Не чіпаємо.
    if not _CYRILLIC.search(w):
        return w

    for suf in _SUFFIXES:
        if w.endswith(suf) and len(w) - len(suf) >= MIN_STEM:
            return w[: -len(suf)]

    return w


def stem_text(text: str) -> str:
    """Токенізує рядок і повертає стемовані токени через пробіл.

    Результат згодовується `to_tsvector('uk', …)` (індекс) або йде в побудову tsquery (запит).
    Пунктуація зникає — вона все одно не потрапила б у tsvector.
    """
    if not text:
        return ""
    return " ".join(stem_word(t) for t in _TOKEN.findall(text))
