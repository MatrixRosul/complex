/**
 * «Не впади через навігацію».
 *
 * ⚠️ Хедер і футер живуть у layout — ВИЩЕ за будь-який error.tsx. Помилка в них не
 * ловиться межею помилок сторінки й валить УВЕСЬ сайт у global-error: користувач не
 * побачить навіть тієї сторінки, дані для якої прекрасно завантажились.
 *
 * Тому дані навігації (дерево категорій, меню, контакти) деградують до фолбеку, а не
 * кидають. Дані САМОЇ сторінки — навпаки: там помилку треба показати чесно, і `safe()`
 * до них не застосовується. Каталог без товарів через збій API мусить сказати про збій,
 * а не вдавати порожню категорію.
 */
export async function safe<T>(promise: Promise<T>, fallback: T): Promise<T> {
  try {
    return await promise;
  } catch (error) {
    console.error("[complex] navigation data unavailable, falling back:", error);
    return fallback;
  }
}
