import { redirect } from "next/navigation";

import { resolveCategoryPathById } from "@/lib/catalog-path";
import { localePath, localeToApiLang, type Locale } from "@/i18n/config";

/**
 * Сумісність зі СТАРИМ форматом посилань на категорію: `/c/{external_id}`.
 *
 * ⚠️ ЧОМУ ЦЕЙ РОУТ ІСНУЄ. Банери в CMS заведені з `link_url = "/uk/c/5609790"` — формат,
 * якого в цьому фронтенді немає (канонічно: `/{locale}/catalog/{slug}/{slug}`). Клік по
 * банеру на головній давав 404. Виправити рядок у БД замовника — не наша справа й не
 * гарантія: контент-менеджер вб'є такий самий шлях завтра. Тому старий вхід лишається
 * робочим і сам веде на канонічну адресу.
 *
 * Редірект ТИМЧАСОВИЙ (307), не постійний: слаг категорії може змінитись, а 308 браузер
 * закешував би назавжди.
 *
 * Другу половину бага — подвійну локаль (`/uk/uk/c/…`) — лікує localePath() (i18n/config.ts).
 */
export default async function LegacyCategoryRedirect({
  params,
}: {
  params: Promise<{ locale: Locale; id: string }>;
}) {
  const { locale, id } = await params;

  const path = await resolveCategoryPathById(id, localeToApiLang[locale]);

  /**
   * ⚠️ КАТЕГОРІЯ ЗНИКЛА → ВЕДЕМО В КАТАЛОГ, А НЕ В 404.
   *
   * Раніше тут стояв `notFound()`, і це рвало головну сторінку по-справжньому: банери в CMS
   * посилаються на `external_id` категорій, а після чистки дерева категорій (видалення
   * демо-гілки 56097xx) ДВА З ТРЬОХ банерів почали вести в нікуди — клік з головної давав
   * порожній 404. Банер — це головний рекламний блок магазину; він не сміє бути глухим кутом
   * лише тому, що контент-менеджер перебрав категорії.
   *
   * Легасі-роут існує саме заради сумісності зі старими посиланнями — тож і поводитись має
   * поблажливо: не знайшли категорію → віддаємо каталог, де людина продовжить шлях сама.
   */
  if (!path) redirect(localePath(locale, "/catalog"));

  redirect(localePath(locale, `/catalog/${path.join("/")}`));
}
